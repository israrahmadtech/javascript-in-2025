// use dates to make your websites dates dynamic
let currentDate = new Date() // constructor
console.log(currentDate) // 2025-03-15T02:27:33.146Z

console.log(currentDate.getMonth()) // 2  :  the index of month is starting from zero
console.log(currentDate.getMilliseconds()) // 949 : one second has 1000 millisecond
console.log(currentDate.getFullYear()) // 2025  : useful at copyright
console.log(currentDate.getDay()) // 6(saturday) taken from device
console.log(currentDate.getDate()) // 15
console.log(currentDate.getMinutes()) // 39
console.log(currentDate.getHours()) // 7
console.log(currentDate.getSeconds()) // 37
console.log(currentDate.getTime()) // 1,742,006,505,501 : time from 1 January 1970 to now : because the date was first time introduced in Unix OS

